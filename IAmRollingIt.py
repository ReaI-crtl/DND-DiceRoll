import random
# num = random.randrange()

def handleInput(inputMessage):
    buffer = input(inputMessage)
    buffer = buffer.lower().split(" ")
    return buffer

def outputMessage(message):
    print(message)

def rollDices(args):
    outputMessage("Format: <number of dices>d<number of sides> (e.g, 2d6 3d20 4d10)")
    outputMessage("Type exit to stop rolling.")
    while True:
        result = 0
        userInput = handleInput("Dice to roll: ")
        if userInput[0] == "exit":
            return

        for dice in userInput:
            bufferResult = 0
            splice = dice.split("d")
            for _ in range(int(splice[0])):
                bufferResult += random.randint(1, int(splice[1]))
            outputMessage(f"Rolling {dice}: {bufferResult}")
            result += bufferResult
        
        outputMessage(f"Result: {result}")

        userInput = handleInput("Bonus (enter to skip): ")
        if userInput[0] == "":
            outputMessage(f"Total result: {result}")
        else:
            outputMessage(f"Total result: {result + int(userInput[0])}")

